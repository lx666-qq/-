void Received_send(void)
{
	if(USART2_RX_STA&0x8000)
	{			
		len=USART2_RX_STA&0x3fff;//得到此次接收到的数据长度
		Flag=USART2_RX_BUF[1];
		V_Tar=(USART2_RX_BUF[2]-48)*100+(USART2_RX_BUF[3]-48)*10+(USART2_RX_BUF[4]-48)+(USART2_RX_BUF[6]-48)*0.1;	
		if(Flag==0x2B)
		{
			TIM_SetCounter(TIM4,0);//确保不会变成无人无车
			OC=1;
			if(OC!=Pre_OC)
			{
				if(V_Tar>6)
				{
					Current_state=1; //右向来车
				}
				if(V_Tar<=6&&V_Tar>0)
				{
					Current_state=2;//右向来人
				}
			}
		 }
			for(t=0;t<len;t++)
			{
				USART_SendData(UART5, USART2_RX_BUF[t]);//串口4发送数据
				while(USART_GetFlagStatus(UART5,USART_FLAG_TC)!=SET);//等待发送结束
			}
			for(a=0;a<2;a++)
			{
				USART_SendData(UART5,RX_BUF[a]);//串口4发送数据
				while(USART_GetFlagStatus(UART5,USART_FLAG_TC)!=SET);//等待发送结束
			}
		USART2_RX_STA=0;
	}

		
	if(USART5_RX_STA&0x8000)
	{			
		len_AC=USART5_RX_STA&0x3fff;//得到此次接收到的数据长度
		Flag_AC=USART5_RX_BUF[1];
		V_Tar_AC=(USART5_RX_BUF[2]-48)*100+(USART5_RX_BUF[3]-48)*10+(USART5_RX_BUF[4]-48)+(USART5_RX_BUF[6]-48)*0.1;	
		if(Flag_AC==0x2B)
		{
			TIM_SetCounter(TIM4,0);//确保不会变成无人无车
			OC_AC=1;
			if(OC_AC!=Pre_OC_AC)
			{
				if(V_Tar_AC>6)
				{
					Current_state_AC=1; //右向来车
				}
				if(V_Tar_AC<=6&&V_Tar_AC>0)
				{
					Current_state_AC=2;//右向来人
				}
			}
		}
		USART5_RX_STA=0;
	}
}

